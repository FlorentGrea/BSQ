#include <bsq.h>

char	*ft_strcat(char *dest, char *src)
{
	char	*ret;

	ret = dest;
	while (*ret)
		ret++;
	while (*src)
		*ret++ = *src++;
	*ret = '\0';
	return (dest);
}
