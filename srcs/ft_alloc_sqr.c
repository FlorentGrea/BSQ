#include <bsq.h>

t_sqr	*ft_alloc_sqr(void)
{
	t_sqr	*sqr;

	if (!(sqr = (t_sqr *)malloc(sizeof(t_sqr))))
		return (0);
	sqr->x = 0;
	sqr->y = 0;
	sqr->line = 0;
	return (sqr);
}
